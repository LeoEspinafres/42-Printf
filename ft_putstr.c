#include "ft_printf.h"

void	ft_putstr(char *c, int *length)
{
	int	i;

	i = 0;
	if (!c)
	{
		*length += write(1, "(null)", 6);
		return ;
	}
	while (c[i] != '\0')
	{
		*length += 1;
		write(1, &c[i], 1);
		i++;
	}
}
